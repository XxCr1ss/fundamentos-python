#Suma y resta (+ y -)
suma = 12 + 3
resta = 12 - 2

#Multiplicacion y division (* y /)
multi = 12 * 3
division = 12 / 4

#Potenciacion (exponente) (**)
exponente = 2**5

#Division baja (//)
divisionBaja = 4 // 2

#Resto o modulo
resto = 12 % 3

#Imprimimos nuestros datos y tipo de datos
miLista = [suma, resta, multi, division, exponente, divisionBaja, resto]
for i in miLista:
    print(i)
for i in miLista:
    print(type(i))
